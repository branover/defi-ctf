#!/usr/bin/env bash
# Pull connection info from the engine and write a .env file for forge/cast.
# Usage:  cd solve/ && ./env.sh
#
# Prerequisites: curl, jq
# Override engine URL:  ENGINE_URL=http://localhost:3000 ./env.sh

set -euo pipefail

ENGINE_URL="${ENGINE_URL:-http://localhost:3000}"
OUTFILE="${1:-.env}"

echo "[env.sh] fetching connection info from ${ENGINE_URL}/api/connection_info ..."

INFO=$(curl -sf "${ENGINE_URL}/api/connection_info") || {
  echo "[env.sh] ERROR: could not reach engine at ${ENGINE_URL}"
  echo "         Is the engine running?  cd engine && npm start"
  exit 1
}

RPC_URL=$(echo "$INFO"        | jq -r '.rpcUrl')
CHAIN_ID=$(echo "$INFO"       | jq -r '.chainId')
PRIVATE_KEY=$(echo "$INFO"    | jq -r '.player.privateKey')
PLAYER_ADDRESS=$(echo "$INFO" | jq -r '.player.address')

{
  echo "# Auto-generated by env.sh — do not commit"
  echo "RPC_URL=${RPC_URL}"
  echo "CHAIN_ID=${CHAIN_ID}"
  echo "PRIVATE_KEY=${PRIVATE_KEY}"
  echo "PLAYER_ADDRESS=${PLAYER_ADDRESS}"
  echo ""
  echo "# Challenge contracts  (names match the challenge manifest's contracts[].id)"
  echo "$INFO" | jq -r '.contracts | to_entries[] | "ADDR_\(.key | ascii_upcase | gsub("-";"_"))=\(.value)"'
  echo ""
  echo "# Token addresses"
  echo "$INFO" | jq -r '.tokens | to_entries[] | "TOKEN_\(.key | ascii_upcase)=\(.value)"'
  echo ""
  echo "# AMM pool addresses + exchange metadata"
  echo "# Each pool exports: POOL_<ID>=address  POOL_<ID>_EXCHANGE=slug  POOL_<ID>_DISPLAY=name  POOL_<ID>_TOKEN_A=sym  POOL_<ID>_TOKEN_B=sym"
  echo "$INFO" | jq -r '
    .pools | to_entries[] |
    ( .key | ascii_upcase | gsub("-";"_") ) as $k |
    ( if (.value | type) == "string"
      then
        # Legacy flat format — address only
        "POOL_\($k)=\(.value)"
      else
        # New rich format — object with address, exchange, displayName, tokenA, tokenB
        "POOL_\($k)=\(.value.address)\nPOOL_\($k)_EXCHANGE=\(.value.exchange)\nPOOL_\($k)_DISPLAY=\"\(.value.displayName)\"\nPOOL_\($k)_TOKEN_A=\(.value.tokenA)\nPOOL_\($k)_TOKEN_B=\(.value.tokenB)"
      end
    )
  '
  # Bot accounts (only present for leverage challenges with botPositions)
  if echo "$INFO" | jq -e '.botAccounts' > /dev/null 2>&1; then
    echo ""
    echo "# Bot accounts (for liquidation challenges)"
    echo "$INFO" | jq -r '.botAccounts | to_entries[] | "BOT_ACCOUNT_\(.key)=\(.value)"'
  fi
} > "$OUTFILE"

echo "[env.sh] wrote ${OUTFILE}"
echo "  RPC_URL        = ${RPC_URL}"
echo "  CHAIN_ID       = ${CHAIN_ID}"
echo "  PLAYER_ADDRESS = ${PLAYER_ADDRESS}"

CONTRACTS=$(echo "$INFO" | jq -r '.contracts | keys | join(", ")')
POOLS=$(echo "$INFO"     | jq -r '.pools     | keys | join(", ")')
[ -n "$CONTRACTS" ] && echo "  Contracts      = ${CONTRACTS}"
[ -n "$POOLS"     ] && echo "  Pools          = ${POOLS}"

echo ""
echo "Next steps:"
echo "  source .env                                        # export vars for cast"
echo "  forge script script/Solve.s.sol --rpc-url \$RPC_URL --private-key \$PRIVATE_KEY --broadcast"
